<p class="heading">What is Discrete Trial Teaching (DTT)?</p>

<p>DTT is a teaching process used to develop most skills, including behavioural, cognitive, communication, play, social, motor development, self-help, and adaptive skills. It is strategy that can be used for all ages and populations and not just for teaching children with autism. Good DTT does not require that a child be sitting at a table or using flashcards and does not involve aversive procedures of any kind. The process takes into account the child’s developmental level and can be very natural and child and family friendly.</p>

<p>The technique involves:</p>

<table class="aba">
	<tr><td class="tableStripe1">breaking a skill into smaller parts</td></tr>
    <tr><td class="tableStripe2">teaching one sub-skill at a time until mastery</td></tr>
    <tr><td class="tableStripe1">allowing repeated practice in a concentrated period of time</td></tr>
    <tr><td class="tableStripe2">providing prompting and prompt fading as necessary</td></tr>
    <tr><td class="tableStripe1">utilizing reinforcement procedures</td></tr>
</table>
<br/>
<p>A teaching session involves numerous trials, with each trial having a distinct beginning and end, hence the name "discrete". Each part of the skill is mastered before more information is presented. In DTT, a very small unit of information is presented and the child's response is immediately sought. This contrasts with continuous trial or more traditional teaching methods, which present large amounts of information with no clearly defined target response on the child's part.</p>

<p>Other techniques used in treatment may include behaviour management, crisis intervention, structured teaching interactions and more traditional counseling.</p>