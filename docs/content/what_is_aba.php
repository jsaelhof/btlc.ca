<div>Choosing an effective treatment for your child requires careful judgment. There are a lot of therapy options out there, and the choices can be overwhelming. Applied Behavior Analysis (ABA) has been shown to be the most researched and scientifically proven treatment to-date. BTLC uses an ABA approach to teaching in our preschool program.</div>

<p>ABA is a field of psychology that focuses on applying learning theories to improve socially significant behavior and bring about positive change.  One specific application of these techniques is intensive behavioural intervention for children with autism. Teaching techniques based on ABA have been shown to significantly improve the abilities of children with autism and other developmental delays to learn and develop.</p>

<p>The techniques of ABA are grounded in science. Some of the hallmarks of ABA include:</p>

<table class="aba">
	<tr><td class="tableStripe1">Breaking complex tasks into smaller tasks that can be taught more easily</td></tr>
    <tr><td class="tableStripe2">Providing repeated opportunities to learn and master new skills</td></tr>
    <tr><td class="tableStripe1">Using reinforcement procedures to assist in the acquisition of new skills</td></tr>
    <tr><td class="tableStripe2">Understanding the functions of behaviours (e.g. communication, attention seeking, avoidance, etc.) and developing programs to meet the child’s needs</td></tr>
    <tr><td class="tableStripe1">Making objective, data driven decisions to guide our evaluations of treatment progress</td></tr>
    <tr><td class="tableStripe2">Utilizing effective teaching techniques, such as discrete trial teaching (DTT)</td></tr>
</table>

