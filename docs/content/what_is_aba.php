<p class="heading">Effectiveness of ABA</p>

<p>Applied Behaviour Analysis (ABA) is based upon more than 50 years of scientific investigation with individuals affected by a wide range of behavioural and developmental disorders. Research has shown ABA to be effective in reducing disruptive behaviours such as self-injury, tantrums, non-compliance, and self-stimulation. ABA has also been shown to be effective in teaching commonly deficient skills such as complex communication, social, play and self-help skills.  Two seminal studies by Lovaas (1987), and McEachin, Smith and Lovaas (1993), have proven the efficacy of ABA. Many studies have also been undertaken since this time which replicate these findings and provide additional support for the techniques.</p>

<br/>

<p class="heading">ABA & Contemporary Behaviour Therapy</p>

<p>BTLC’s approach to ABA treatment of autism differs in many fundamental ways from the more rigid application of ABA. Therefore, to distinguish our approach we identify it as Contemporary Behavior Therapy (CBT).</p>

<p>Some of the hallmarks of CBT include:</p>

<table class="aba">
	<tr><td class="tableStripe1">Utilization of all the guiding principles of ABA with the benefit of the clever innovations of thousands of individuals over the years</td></tr>
    <tr><td class="tableStripe2">Emphasis on a positive and systematic approach to teaching functional skills and reducing behaviour problems</td></tr>
    <tr><td class="tableStripe1">Therapy is a creative process and is implemented with structured flexibility, capitalizing on the resources available for each individual child</td></tr>
    <tr><td class="tableStripe2">While we have found certain teaching techniques to be consistently effective, we also recognize that each person working with a child has their own style and unique contribution to make to the educational treatment process</td></tr>
    <tr><td class="tableStripe1">Our general approach is very pragmatic: if it works, stick with it; if it does not work, figure out how to fix it!</td></tr>
</table>

